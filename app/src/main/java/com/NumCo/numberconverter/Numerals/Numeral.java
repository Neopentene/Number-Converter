package com.NumCo.numberconverter.Numerals;

public class Numeral {
    public String value;

    public Numeral(String value) {
        this.value = value;
    }

    public String toDec() {
        return value;
    }

    public String toHex() {
        return value;
    }

    public String toBin() {
        return value;
    }

    public String toOct() {
        return value;
    }

}
